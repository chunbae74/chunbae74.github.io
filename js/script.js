/* — Contact ——————————————————————————————————————————————————  */
function renderContact(meta) {
  el('contact-lead').textContent = meta.contactLead;

  // Info grid: only render rows that have a value
  const rows = [
    { label: 'Email',       value: meta.email,       href: `mailto:${meta.email}` },
    { label: 'Affiliation', value: meta.affiliation,  href: null },
    { label: 'Office',      value: meta.office,       href: null },
  ].filter(r => r.value);

  el('contact-grid').innerHTML = rows.map(r => `
    <div class="contact-item">
      <span class="contact-label">${esc(r.label)}</span>
      ${r.href
        ? `<a href="${esc(r.href)}" class="text-link-on-dark contact-value">${esc(r.value)}</a>`
        : `<span class="contact-value">${esc(r.value)}</span>`}
    </div>
  `).join('');

  // Social buttons: only render links that have a value
  const socials = [
    { label: 'GitHub',   url: meta.github },
    { label: 'LinkedIn', url: meta.linkedin },
    { label: 'Blog',     url: meta.blog },
  ].filter(s => s.url);

  el('contact-links').innerHTML = socials.map(s => `
    <a href="${esc(s.url)}" class="btn btn-secondary-dark" target="_blank" rel="noopener">${esc(s.label)}</a>
  `).join('');
}


/* ══════════════════════════════════════════════════════════════
   NAVIGATION BEHAVIOUR
   (hamburger + active-section highlight)
══════════════════════════════════════════════════════════════ */

/* — Mobile hamburger ————————————————————————————————————————— */
const hamburger = el('nav-hamburger');
const drawer    = el('nav-mobile-drawer');

if (hamburger && drawer) {
  const closeDrawer = () => {
    hamburger.classList.remove('open');
    drawer.classList.remove('open');
    hamburger.setAttribute('aria-expanded', 'false');
    drawer.setAttribute('aria-hidden', 'true');
    document.body.style.overflow = '';
  };

  hamburger.addEventListener('click', () => {
    const isOpen = hamburger.classList.toggle('open');
    drawer.classList.toggle('open', isOpen);
    hamburger.setAttribute('aria-expanded', String(isOpen));
    drawer.setAttribute('aria-hidden', String(!isOpen));
    document.body.style.overflow = isOpen ? 'hidden' : '';
  });

  drawer.querySelectorAll('a').forEach(a => a.addEventListener('click', closeDrawer));
  document.addEventListener('keydown', e => { if (e.key === 'Escape') closeDrawer(); });
}


/* — Active-section highlight (called after content renders) —— */
function initScrollObserver() {
  const navLinks = document.querySelectorAll('.nav-link');
  const linkMap  = {};
  navLinks.forEach(l => { linkMap[l.getAttribute('href').slice(1)] = l; });

  const observer = new IntersectionObserver(entries => {
    entries.forEach(entry => {
      const link = linkMap[entry.target.id];
      if (!link) return;
      if (entry.isIntersecting) {
        navLinks.forEach(l => l.classList.remove('active'));
        link.classList.add('active');
      }
    });
  }, { rootMargin: '-44px 0px -55% 0px', threshold: 0 });

  document.querySelectorAll('section[id]').forEach(s => observer.observe(s));
}


/* ══════════════════════════════════════════════════════════════
   BOOT — fetch data.json then render everything
══════════════════════════════════════════════════════════════ */
fetch('data.json')
  .then(r => {
    if (!r.ok) throw new Error(`HTTP ${r.status}`);
    return r.json();
  })
  .then(data => {
    renderMeta(data.meta);
    renderHero(data.meta);
    renderAbout(data.about);
    renderEducation(data.education);
    renderPublications(data.publications, data.meta.name);
    renderProjects(data.projects);
    renderExperience(data.experience);
    renderAwards(data.awards);
    renderContact(data.meta);
    initScrollObserver();
  })
  .catch(err => {
    console.error('[CV] Could not load data.json:', err.message);
    console.info('[CV] Serve the site via a local server:');
    console.info('     python3 -m http.server 8000');
  });
