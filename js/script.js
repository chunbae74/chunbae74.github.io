/* ── Utilities ───────────────────────────────────────────────── */

function el(id) { return document.getElementById(id); }

// Escape plain text before inserting into HTML
function esc(str) {
  return String(str)
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;');
}

// Bold every occurrence of `name` inside an author string
function boldSelf(authors, name) {
  if (!name) return esc(authors);
  return authors
    .split(', ')
    .map(a => a.trim() === name.trim()
      ? `<strong>${esc(a)}</strong>`
      : esc(a))
    .join(', ');
}


/* ══════════════════════════════════════════════════════════════
   RENDER FUNCTIONS
   One function per section. Each function receives the relevant
   slice of data.json and writes into the matching DOM container.
══════════════════════════════════════════════════════════════ */

/* — Meta: nav logo, page title, footer name ————————————————— */
function renderMeta(meta) {
  // Page <title> and <meta description>
  el('page-title').textContent       = `${meta.name} — Academic CV`;
  el('page-description').content     = `${meta.name} — ${meta.title}`;

  // Nav logo
  el('nav-logo').textContent         = meta.name;

  // Footer
  el('footer-name').textContent      = meta.name;
  el('footer-year').textContent      = new Date().getFullYear();
}


/* — Hero ———————————————————————————————————————————————————— */
function renderHero(meta) {
  el('hero-initials').textContent = meta.initials || '';
  el('hero-name').textContent     = meta.name;
  el('hero-title').textContent    = meta.title;
  el('hero-intro').textContent    = meta.intro;

  // Build CTA row: Email button + text links
  const links = [];
  if (meta.github)   links.push(`<a href="${esc(meta.github)}"   class="text-link" target="_blank" rel="noopener">GitHub</a>`);
  if (meta.linkedin) links.push(`<a href="${esc(meta.linkedin)}"  class="text-link" target="_blank" rel="noopener">LinkedIn</a>`);
  if (meta.blog)     links.push(`<a href="${esc(meta.blog)}"      class="text-link" target="_blank" rel="noopener">Blog</a>`);

  el('hero-actions').innerHTML = `
    <a href="mailto:${esc(meta.email)}" class="btn btn-primary">Email</a>
    ${links.length ? `<div class="hero-links">${links.join('')}</div>` : ''}
  `;
}


/* — About ——————————————————————————————————————————————————— */
function renderAbout(about) {
  el('about-summary').textContent = about.summary;

  el('about-skills').innerHTML = about.skills
    .map(s => `<span class="chip">${esc(s)}</span>`)
    .join('');

  el('about-interests').innerHTML = about.interests
    .map(i => `<span class="chip">${esc(i)}</span>`)
    .join('');
}


/* — Education ——————————————————————————————————————————————— */
function renderEducation(education) {
  el('education-list').innerHTML = education.map(entry => `
    <article class="timeline-entry">
      <div class="timeline-marker" aria-hidden="true"></div>
      <div class="timeline-body">
        <div class="timeline-header">
          <div>
            <h3 class="timeline-title">${esc(entry.degree)}</h3>
            <p  class="timeline-org">${esc(entry.institution)}</p>
          </div>
          <span class="timeline-date">${esc(entry.period)}</span>
        </div>
        <ul class="timeline-details">
          ${entry.details.map(d => `<li>${esc(d)}</li>`).join('')}
        </ul>
      </div>
    </article>
  `).join('');
}


/* — Publications ————————————————————————————————————————————— */
function renderPublications(publications, selfName) {
  // Hide the entire section when there are no publications
  if (!publications || publications.length === 0) {
    const section = document.getElementById('research');
    if (section) section.style.display = 'none';
    return;
  }

  el('pub-list').innerHTML = publications.map(pub => {
    const linksHtml = pub.links
      .map(l => `<a href="${esc(l.url)}" class="pub-link" target="_blank" rel="noopener">${esc(l.label)}</a>`)
      .join('');

    return `
      <article class="pub-entry">
        <div class="pub-meta">
          <span class="pub-year">${esc(String(pub.year))}</span>
          <span class="pub-venue">${esc(pub.venue)}</span>
        </div>
        <div class="pub-body">
          <h3 class="pub-title">${esc(pub.title)}</h3>
          <p  class="pub-authors">${boldSelf(pub.authors, selfName)}</p>
          <div class="pub-links">${linksHtml}</div>
        </div>
      </article>
    `;
  }).join('');
}


/* — Projects ————————————————————————————————————————————————— */
function renderProjects(projects) {
  el('project-grid').innerHTML = projects.map(p => {
    // Right side of the project header: link or status badge
    const headerRight = p.link
      ? `<a href="${esc(p.link)}" class="text-link project-ext-link" target="_blank" rel="noopener">${esc(p.linkLabel || '↗')}</a>`
      : (p.status ? `<span class="project-status-tag">${esc(p.status)}</span>` : '');

    const toolsHtml = p.tools
      .map(t => `<span class="chip chip-sm">${esc(t)}</span>`)
      .join('');

    return `
      <article class="project-card">
        <div class="project-header">
          <h3 class="project-title">${esc(p.title)}</h3>
          ${headerRight}
        </div>
        <p class="project-desc">${esc(p.description)}</p>
        <div class="project-tools">${toolsHtml}</div>
        <p class="project-outcome"><strong>Outcome:</strong> ${esc(p.outcome)}</p>
      </article>
    `;
  }).join('');
}


